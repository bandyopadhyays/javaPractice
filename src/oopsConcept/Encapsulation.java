package oopsConcept;

public class Encapsulation {

	/**
	 * Encapsulation is achieved through Information Hiding, and specifically
	 * around hiding internal data (fields / members representing the state)
	 * owned by a class instance, by enforcing access to the internal data in a
	 * controlled manner, and preventing direct, external change to these
	 * fields, as well as hiding any internal implementation methods of the
	 * class (e.g. by making them private).
	 * 
	 * For example, a classes' fields can be made private by default, and only
	 * if external access to these was required, would a get() and/or set() (or
	 * Property) be exposed from the class. (In modern day OO languages, fields
	 * can be marked as read only / final / immutable which further restricts
	 * change, even within the class).
	 * 
	 * Binding code and data together into a single unit is known as
	 * Encapsulation. Like a wrapper. Example – Class is a classic example of
	 * encapsulation.
	 */

}
